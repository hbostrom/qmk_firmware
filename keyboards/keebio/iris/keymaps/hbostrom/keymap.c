bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOCAL:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case REMOTE:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY_REMOTE);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case I3LOC:
      if (record->event.pressed) {
        add_key(KC_LGUI);
        send_keyboard_report();
      } else {
        del_key(KC_LGUI);
        send_keyboard_report();
      }
      return false;
      break;
    case I3REM:
      if (record->event.pressed) {
        add_key(KC_LCTL);
        add_key(KC_LALT);
        add_key(KC_LGUI);
        send_keyboard_report();
      } else {
        del_key(KC_LCTL);
        del_key(KC_LALT);
        del_key(KC_LGUI);
        send_keyboard_report();
      }
      return false;
      break;
    case CTLMIN:
      if (record->event.pressed) {
        add_key(KC_LCTL);
        add_key(KC_MINS);
        send_keyboard_report();
      } else {
        del_key(KC_LCTL);
        del_key(KC_MINS);
        send_keyboard_report();
      }
      return false;
      break;
    case CTLPLS:
      if (record->event.pressed) {
        add_key(KC_LCTL);
        add_key(KC_LSFT);
        add_key(KC_EQL);
        send_keyboard_report();
      } else {
        del_key(KC_LCTL);
        del_key(KC_LSFT);
        del_key(KC_EQL);
        send_keyboard_report();
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
